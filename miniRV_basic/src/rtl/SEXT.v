// ============================================================================
// SEXT.v — 立即数符号扩展模块 (Sign Extension)
// ============================================================================
// 【功能】RISC-V 指令中有5种不同的立即数编码格式，本模块根据 op 信号
//   从 32 位指令的 inst[31:7] 字段中提取并符号扩展为 32 位的立即数。
//
// 【5种立即数格式及其在指令中的位分布】
//                   31   30   25   24   21   20   19   15   14   12   11   8   7
//   I-type (EXT_I): |<-------- imm[11:0] -------->|  inst[31:20]直接符号扩展
//   S-type (EXT_S): |imm[11:5]|      |imm[4:0]|    inst[31:25] + inst[11:7]
//   B-type (EXT_B): |imm[12]|imm[10:5]|   |imm[4:1]|imm[11]  存储格式(末尾补0)
//   U-type (EXT_U): |<----- imm[31:12] ----->|      inst[31:12] + 低12位补0
//   J-type (EXT_J): |imm[20]|imm[10:1]|imm[11]|imm[19:12]   存储格式(末尾补0)
//
// 【理解重点】
//   1. B-type 和 J-type 的立即数末尾隐含一个 0 位（16位对齐），所以拼接时末尾补 1'b0
//   2. 符号扩展方式：取立即数的最高位，复制填充高位
//   3. U-type 用于 LUI/AUIPC，低12位补0（相当于左移12位）
//   * S-type 的 EXT_S 位索引在之前的版本可能不正确，请参考上面的分布图
// ============================================================================

`timescale 1ns / 1ps

`include "defines.vh"

module SEXT (
    input  wire [ 2:0]  op,        // 操作类型：I/S/B/U/J
    input  wire [31:7]  imm,       // 指令高位部分（inst[31:7]），低位 opcode 不需要
    output reg  [31:0]  ext        // 扩展后的32位立即数
);

    always @(*) begin
        case (op)
            // I-type: 12位立即数在 inst[31:20]，符号扩展
            `EXT_I : ext = {{20{imm[31]}}, imm[31:20]};

            // S-type: 12位立即数，高位在 inst[31:25]，低位在 inst[11:7]
            `EXT_S : ext = {{20{imm[31]}}, imm[31:25], imm[11:7]};

            // B-type: 13位立即数，但bit[0]隐含为0（分支目标总是2字节对齐）
            // 存储位序与编码位序不同，需要重新排列（注意：这里的位索引对应关系需要参照RISC-V规范核对）
            `EXT_B : ext = {{19{imm[31]}}, imm[31], imm[7], imm[30:25], imm[11:8], 1'b0};

            // U-type: 20位立即数在 inst[31:12]，低12位补0
            `EXT_U : ext = {imm[31:12], 12'h0};

            // J-type: 21位立即数，bit[0]隐含为0
            // JAL的跳转范围是 ±1MB
            `EXT_J : ext = {{11{imm[31]}}, imm[31], imm[19:12], imm[20], imm[30:21], 1'b0};

            default: ext = 32'h0;
        endcase
    end

endmodule
