// ============================================================================
// RF.v — 寄存器堆模块 (Register File)
// ============================================================================
// 【功能】32个32位通用寄存器的读写
// 【关键特性】
//   1. x0 寄存器硬连线为 0 —— 无论写什么，读 x0 永远返回 0
//      这是 RISC-V 架构的规定（x0 = zero register）
//   2. 写操作在时钟上升沿完成（时序逻辑）
//   3. 读操作是组合逻辑（异步读），当拍给出结果
// 【理解重点】
//   - rf_we 来自 Controller，只控制是否写寄存器（不是读）
//   - 读端口有2个（rR1→rD1, rR2→rD2），支持同时读两个操作数
//   - regs 数组下标1-31对应 x1-x31，下标0不存在，通过 (rR1==0)?0:... 实现
// ============================================================================

`timescale 1ns / 1ps

module RF (
    input  wire         clk,         // 时钟
    input  wire [ 4:0]  rR1,         // 读端口1地址（inst[19:15] = rs1）
    input  wire [ 4:0]  rR2,         // 读端口2地址（inst[24:20] = rs2）
    input  wire         we,          // 写使能（高有效）
    input  wire [ 4:0]  wR,          // 写地址   （inst[11:7] = rd）
    input  wire [31:0]  wD,          // 写数据   （来自写回MUX）

    output wire [31:0]  rD1,         // 读端口1数据
    output wire [31:0]  rD2          // 读端口2数据
);

    reg [31:0] regs [1:31];           // 32个寄存器，x0不使用数组下标

    // 异步读：x0返回0，其他寄存器直接输出
    assign rD1 = (rR1 == 0) ? 0 : regs[rR1];
    assign rD2 = (rR2 == 0) ? 0 : regs[rR2];

    // 同步写：时钟上升沿写入，不能写x0（x0永远是0）
    always @(posedge clk) begin
        if (we && (wR != 5'h0))
            regs[wR] <= wD;
    end

endmodule
